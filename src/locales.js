let currentLocale = DEFAULT_LOCALE;

export function getCurrentLocale() {
  return currentLocale;
}

export function setCurrentLocale(locale) {
  currentLocale = locales[locale] ? locale : DEFAULT_LOCALE;
}

export function getLocaleMeta(locale = currentLocale) {
  return locales[locale]?.meta || locales[DEFAULT_LOCALE].meta;
}

export function getLocaleSwitchOptions() {
  return Object.values(locales).map(({ meta }) => ({
    locale: meta.code,
    label: meta.label,
  }));
}

export function getLocaleValue(key, locale = currentLocale) {
  return key.split(".").reduce((value, part) => value?.[part], locales[locale]);
}
